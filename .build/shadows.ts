export const Shadows = {
  /** Mid elevation - elements appear more raised and interactive.
  - For all interactive elements that require a shadow.
  - e.g. interactive cards, carousel buttons, etc. */
      interactive,
  /** High elevation - elements that sit above main body contents.
  - For elements that stick to the top of the screen upon scroll. This shadow only appears when the user scrolls down the screen.
  - e.g. top navigation bar */
      sticky_top,
  /** High elevation - elements that sit above main body contents.
  - For elements that stick to the bottom of the screen.
  - e.g. bottom navigation, sticky-bottom button groups */
      sticky_bottom,
  /** Very high elevation - elements that sit above all elements.
  - e.g. support chat FAB, toasts, tooltips, modals */
      float,
      keyDropS,
      keyDropM,
      keyDropL,
      keyInnerS,
      keyInnerM,
      keyInnerL,
      keyReverseS,
      keyReverseM,
      keyReverseL,
      ambientDropS,
      ambientDropM,
      ambientDropL,
      ambientReverseS,
      ambientReverseM,
      ambientReverseL,
  /** Sunken - for elements that appear below surfaces with no elevation.
  
  - Use to subdue an element.
  - No-elevation surfaces will cover the element once scrolled.
  - Use this shadow on elements that sit beside no-elevation surfaces. Do not apply sunken shadow on raised elevation surfaces.
  - e.g. alert banner */
      sunken,
  /** Low elevation - elements appear slightly raised but in close elevation from the surface.
  - For non-interactive elements.
  - e.g. non-interactive cards, containers, etc. */
      nonInteractive,
      componentCard,
}